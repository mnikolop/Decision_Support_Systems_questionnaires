          <li>Headache</li>
            <ol class="r">
              <li>Episodic Tension Headaches (occur less than 15 days per month)</li>
              <ul class="a">
                <li>Pain is mild to moderate, constant band-like pain, pressure or throbbing  </li>
                <li>Pain affects the front, top or sides of the head</li>
                <li>Pain usually begins gradually, and often occurs in the middle of the day  </li>
                <li>Pain may last from 30 minutes to several days  </li>
              </ul>
              <li>Chronic Tension Headaches (occur more than 15 days per month)</li>
              <ul class="a">
                <li>Pain may vary in intensity throughout the day, but the pain is almost always present </li>
                <li>Pain affects the front, top or sides of the head </li>
                <li>Pain comes and goes over a prolonged period of time </li>
              </ul>
              <li>Associated Symptoms of Tension Headaches</li>
              <ul class="a">
                <li>Headache upon awakening </li>
                <li>Difficulty falling asleep and staying asleep </li>
                <li>Chronic fatigue  </li>
                <li>Irritability </li>
                <li>Disturbed concentration </li>
                <li>Mild sensitivity to light or noise </li>
                <li>General muscle aching </li>
              </ul>
              <li>Migraines</li>
              <ul class="a">
                <li>Moderate to severe pain (often described as pounding, throbbing pain) that can affect the   whole head, or can shift from one side of the head to the other </li>
                <li>Sensitivity to light, noise or odors  </li>
                <li>Blurred vision   </li>
                <li>Nausea or vomiting, stomach upset, abdominal pain  </li>
                <li>Loss of appetite  </li>
                <li>Sensations of being very warm or cold  </li>
                <li>Paleness  </li>
                <li>Fatigue  </li>
                <li>Dizziness  </li>
                <li>Fever (rare)  </li>
                <li>Bright flashing dots or lights, blind spots, wavy or jagged lines (aura)  </li>
              </ul>
              <li>Cluster Headaches</li>
              <ul class="a">
                <li>Intense one-sided pain described as having a burning or piercing quality that is throbbing or constant  </li>
                <li>Pain is located behind one eye or in the eye region, without changing sides</li>
                <li>Pain lasts a short time, generally 30 to 90 minutes (but can last for three hours);  the headache will disappear, only to recur later that day</li>
                <li>Headaches occur very regularly, generally at the same time each day, and they often awaken the person at the same time during the night</li>
              </ul>
              <li>Sinus Headaches</li>
              <ul class="a">
                <li>Deep and constant pain in the cheekbones, forehead or bridge of the nos</li>
                <li>The pain usually intensifies with sudden head movement or straining and usually occurs with other sinus symptoms, such as nasal discharge, feeling of fullness in the ears, fever, and facial swelling</li>
              </ul>
