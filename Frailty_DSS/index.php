					<p>
						Frailty is a common geriatric syndrome that embodies an elevated risk of catastrophic declines in health and function 
						among elderly people. As our population ages, a central focus of geriatricians and public health practitioners is to understand, 
						and then beneficially intervene on, the factors and processes that put elders in the community at such risk, in particular the 
						increased vulnerability to stressors that characterizes many older adults. Worldwide, at least  35.6 million people have various 
						geriatric symptoms, with just over half (58%) living in low- and middle-income countries. Every year, there are 7.7 million new cases. 
						The estimated proportion of the general population aged 60 and over with geriatric symptoms centralized by their frailty condition 
						at a given time is between 2 to 8 per 100 people. The total number of people with geriatric symptoms is projected to almost double 
						every 20 years, to 65.7 million in 2030 and 115.4 million in 2050. Much of this increase is attributable to the fact of the rising 
						numbers of elderly people among the world. Thus, the need for their cure and care is of a highly concern. 
						<br>
						With no corresponding Clinical Decision Support System (CDSS) in geriatric’s discipline, we find crucial to develop a CDSS to help 
						those in need.A CDSS is a computer program application that analyzes clinical data and presents it so that users can make clinical 
						decisions more easily. CDSS is an interactive decision support system Computer Software, which is designed to assist physicians 
						and other health professionals with decision making tasks, such as determining diagnosis of patient data. Physicians, nurses and 
						other health care professionals use a CDSS to prepare a diagnosis and to review the diagnosis as a means of improving the final result. 
						Data mining may be conducted to examine the patient’s medical history in conjunction with relevant clinical research. The widespread 
						usage of CDSS is already registered and is being used as a mean to improve health care and well being.
						<br>
						In this paper, we extended the well known 70-scale Canadian Research and formulate a 7-category of elderly geriatric 
						characterization and assessment (7-CoECA). In order to evaluate a clinical study, we designed a novel CDSS. Moreover, a website in 
						which the patient’s risk factors are considered so as to make the diagnosis more reliable, is under development by our team. 
						Both the website and this new classification of frailty symptoms as analyzed below could aid the activity of geriatricians and 
						medical doctors. 
